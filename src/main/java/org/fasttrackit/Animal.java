package org.fasttrackit;

public class Animal {

    String name;
    int age;
    int healthLevel;
    int hungerLevel;
    int moodLevel;
    String favouriteFood;
    String favouriteGame;

    public Animal(String name, String favouriteFood, String favouriteGame) {
        this.name = name;
        this.favouriteFood = favouriteFood;
        this.favouriteGame = favouriteGame;
    }

}
